# Practicando con Strings
# Utilizamos el símbolo + para concatenar textos

print("Hola " + "Eulogio")

# Con \n podemos hacer saltos de líneas
print("Esta es una línea\nY esta es otra línea")

# Con \t podemos tabular la línea

print("\tEsta es la tercera línea")

# En general, con la barra invertida \, podemos añadir caracteres especiales

print('This isn\'t a number')
print("Este signo \\ es una barra invertida")