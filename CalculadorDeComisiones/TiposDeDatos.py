# En Python hay una variedad de tipos de datos:
#
# Cadena de caractéres: string (str)      "hola", "%$&", " ", "123"

# Números enteros:      integer (int)     150, 1, 555, -15, 0

# Números decimales:    floating (float)  1.25, 25.0, 500.50, -95.1

# Listas donde pueden contener varios tipos de datos. Tienen un índice que empieza
# por 0: (lst) ["sal", 1, 4.5, "marte"]

# Diccionarios donde hay pares de palabras agrupadas, clave : valor. No hay índice
# de orden: (dic) {'color':'rojo', 'arte':'cine'}

# Tuples. El orden de los datos que contiene es inmutable. No se puede modificar su posición:
# (tup) ('lun','mar','mie','jue','vier')

# Sets. Es un conjunto de elementos ordenados ÚNICOS. A diferencia de las Listas o los Tuples,
# en los Sets no pueden contener elementos repetidos: (set) {'a','b','c','d','e'}

# Booleanos. Valores lógicos de True o False: (bool) True, False